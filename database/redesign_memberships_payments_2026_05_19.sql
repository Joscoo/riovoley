COMMENT ON COLUMN billing.membership_types.nombre IS 'Nombre visible del tipo de mensualidad';
COMMENT ON COLUMN billing.membership_types.detalle IS 'Descripcion corta del plan';
COMMENT ON COLUMN billing.membership_types.costo IS 'Costo oficial de la mensualidad';

