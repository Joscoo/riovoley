-- 4) Backfill historico de membership_type_id
WITH membership_ids AS (
  SELECT
    MAX(CASE WHEN code = 'normal' THEN id END) AS normal_id,
    MAX(CASE WHEN code = 'grupo' THEN id END) AS grupo_id
  FROM billing.membership_types
)
