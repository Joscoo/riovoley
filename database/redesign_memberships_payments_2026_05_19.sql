-- 5) Funciones para calculo de periodo y estado
CREATE OR REPLACE FUNCTION public.calculate_membership_payment_period(
  p_student_id uuid,
  p_fecha_pago date,
  p_exclude_payment_id uuid DEFAULT NULL
) RETURNS TABLE(fecha_inicio date, fecha_fin date)
LANGUAGE plpgsql
AS $$
DECLARE
  v_last_end date;
  v_start date;
BEGIN
  SELECT MAX(p.fecha_fin)
  INTO v_last_end
  FROM billing.payments p
  WHERE p.student_id = p_student_id
    AND p.deleted_at IS NULL
    AND p.id IS DISTINCT FROM p_exclude_payment_id;

  v_start := COALESCE(v_last_end + 1, COALESCE(p_fecha_pago, CURRENT_DATE));

  RETURN QUERY
  SELECT
    v_start,
    (v_start + INTERVAL '1 month' - INTERVAL '1 day')::date;
END;
$$;

CREATE OR REPLACE FUNCTION public.calculate_payment_state(
  p_fecha_fin date
) RETURNS text
LANGUAGE plpgsql
AS $$
DECLARE
  v_diff integer;
BEGIN
  IF p_fecha_fin IS NULL THEN
    RETURN 'activo';
  END IF;

  v_diff := p_fecha_fin - CURRENT_DATE;

  IF v_diff < 0 THEN
    RETURN 'vencido';
  END IF;

  IF v_diff <= 5 THEN
    RETURN 'proximo_a_vencer';
  END IF;

  RETURN 'activo';
END;
$$;

-- 6) Trigger de automatizacion
CREATE OR REPLACE FUNCTION public.apply_payment_membership_rules()
RETURNS trigger
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public, billing, pg_temp
AS $$
DECLARE
  v_cost numeric(10,2);
  v_should_recompute_period boolean;
  v_start date;
  v_end date;
BEGIN
  IF NEW.student_id IS NULL THEN
    RAISE EXCEPTION 'student_id es obligatorio para registrar pagos';
  END IF;

  IF NEW.membership_type_id IS NULL THEN
    RAISE EXCEPTION 'membership_type_id es obligatorio para registrar pagos';
  END IF;

  NEW.fecha_pago := COALESCE(NEW.fecha_pago, CURRENT_DATE);

  SELECT mt.costo
  INTO v_cost
  FROM billing.membership_types mt
  WHERE mt.id = NEW.membership_type_id
    AND mt.active = true;

  IF v_cost IS NULL THEN
    RAISE EXCEPTION 'El tipo de mensualidad % no existe o esta inactivo', NEW.membership_type_id;
  END IF;

  NEW.monto := v_cost;

  IF TG_OP = 'INSERT' THEN
    v_should_recompute_period := true;
  ELSE
    v_should_recompute_period :=
      NEW.student_id IS DISTINCT FROM OLD.student_id
      OR NEW.membership_type_id IS DISTINCT FROM OLD.membership_type_id
      OR NEW.fecha_inicio IS NULL
      OR NEW.fecha_fin IS NULL;
  END IF;

  IF v_should_recompute_period THEN
    SELECT p.fecha_inicio, p.fecha_fin
    INTO v_start, v_end
    FROM public.calculate_membership_payment_period(
      NEW.student_id,
      NEW.fecha_pago,
      CASE WHEN TG_OP = 'UPDATE' THEN NEW.id ELSE NULL END
    ) p;

    NEW.fecha_inicio := v_start;
    NEW.fecha_fin := v_end;
  END IF;

  IF NEW.fecha_fin IS NOT NULL AND NEW.fecha_inicio IS NOT NULL AND NEW.fecha_fin < NEW.fecha_inicio THEN
    RAISE EXCEPTION 'fecha_fin no puede ser menor a fecha_inicio';
  END IF;

  NEW.estado := public.calculate_payment_state(NEW.fecha_fin);

  RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS payments_membership_rules_trigger ON billing.payments;
CREATE TRIGGER payments_membership_rules_trigger
BEFORE INSERT OR UPDATE ON billing.payments
FOR EACH ROW
EXECUTE FUNCTION public.apply_payment_membership_rules();

-- Permisos minimos para lectura desde API sin romper trigger/rules
GRANT USAGE ON SCHEMA billing TO authenticated;
GRANT SELECT ON billing.membership_types TO authenticated;

-- 7) Normalizar registros legacy incompletos
UPDATE billing.payments p
SET membership_type_id = p.membership_type_id
WHERE p.deleted_at IS NULL
  AND (p.fecha_inicio IS NULL OR p.fecha_fin IS NULL OR p.estado IS NULL);

-- 8) Sincronizar estado historico al momento de migrar
UPDATE billing.payments p
SET estado = public.calculate_payment_state(p.fecha_fin)
WHERE p.deleted_at IS NULL;

COMMIT;
